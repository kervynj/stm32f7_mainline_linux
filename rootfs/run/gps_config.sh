#!/bin/sh

#disable NMEA default messages, leave only GPS 
echo -ne '\xB5\x62\x06\x00\x14\x00\x01\x00\x00\x00\xD0\x08\x00\x00\x80\x25\x00\x00\x23\x00\x00\x00\x00\x00\x00\x00\BB\x83' > /dev/ttySTM1
echo -ne '\xB5\x62\x06\x01\x08\x00\xF0\x03\x00\x00\x00\x00\x00\x00\x02\x38' > /dev/ttySTM1 #disable NMEA-GxGSV
echo -ne '\xB5\x62\x06\x01\x08\x00\xF0\x01\x00\x00\x00\x00\x00\x00\x00\x2A' > /dev/ttySTM1 #disable NMEA-GxGLL
echo -ne '\xB5\x62\x06\x01\x08\x00\xF0\x05\x00\x00\x00\x00\x00\x00\x04\x46' > /dev/ttySTM1 #disable NMEA-GxVTG
echo -ne '\xB5\x62\x06\x01\x08\x00\xF0\x04\x00\x00\x00\x00\x00\x00\x03\x3F' > /dev/ttySTM1 #disable NMEA-GxRMC
echo -ne '\xB5\x62\x06\x01\x08\x00\xF0\x02\x00\x00\x00\x00\x00\x00\x01\x31' > /dev/ttySTM1 #disable NMEA-GxGSA
echo -ne '\xB5\x62\x06\x01\x08\x00\xF0\x08\x00\x01\x00\x00\x00\x00\x08\x60' > /dev/ttySTM1 #disable NMEA-GxZDA

# configure receiver GNSS
echo -ne '\xB5\x62\x06\x3E\x0C\x00\x00\x00\x00\x01\x00\x04\x0A\x00\x01\x00\x00\x01\x61\x10' > /dev/ttySTM1 #enable ONLY GPS, 4-10 channels


